                    <br>
                    <h4>BAB I</h4>
                    <h4>KETENTUAN UMUM</h4>
                </div>
                <div class="mt-10">
                    <strong>Pasal 1</strong>
                    <br>
                    <strong>Dalam Peraturan Bupati ini yang dimaksud dengan :</strong>
                    <ol>
                        <li>Daerah adalah Kabupaten Banyuwangi.</li>
                        <li>Pemerintah Daerah adalah Pemerintah Kabupaten Banyuwangi.</li>
                        <li>Bupati adalah Bupati Banyuwangi.</li>
                        <li>Sekretaris Daerah adalah Sekretaris Daerah Kabupaten Banyuwangi.</li>
                        <li>Perangkat Daerah adalah unsur pembantu Bupati dalam penyelenggaraan pemerintahan daerah, yang terdiri dari Sekretariat Daerah, Sekretariat Dewan Pewakilan Rakyat Daerah, Dinas Daerah, Lembaga Teknis Daerah, Kecamatan dan Kelurahan.</li>
                        <li>Informasi adalah keterangan, pernyataan, gagasan dan tanda-tanda yang mengandung nilai, makna dan pesan, baik data, fakta maupun penjelasannya yang dapat dilihat, didengar dan dibaca, yang disajikan dalam berbagai kemasan dan format sesuai dengan perkembangan teknologi informasi dan komunikasi secara elektronik maupun non elektronik; perkembangan teknologi informasi dan komunikasi secara elektronik maupun non elektronik.</li>
                        <li>Dokumentasi adalah pengumpulan, pengolahan, penyusunan dan pencatatan dokumen, data, gambar dan suara untuk bahan informasi publik.</li>
                        <li>Informasi Publik adalah informasi yang dihasilkan, disimpan, dikelola, dikirim dan/atau diterima oleh Pemerintah Daerah yang berkaitan dengan penyelenggaraan Pemerintah Daerah sesuai dengan peraturan perundang-undangan serta informasi lain yang berkaitan dengan kepentingan publik.</li>
                        <li>Badan Publik adalah lembaga eksekutif, legislatif dan badan lain yang fungsi dan tugas pokoknya berkaitan dengan penyelenggaraan negara, yang sebagian atau seluruh dananya bersumber dari Anggaran Pendapatan dan Belanja Negara dan/atau Anggaran Pendapatan dan Belanja Daerah, atau organisasi non pemerintah sepanjang sebagian atau seluruh dananya bersumber dari Anggaran Pendapatan dan Belanja Negara dan/atau Anggaran Pendapatan dan Belanja Daerah, sumbangan masyarakat, dan/atau luar negeri.</li>
                        <li>Pejabat publik adalah orang yang ditunjuk dan diberi tugas untuk menduduki posisi atau jabatan tertentu pada Badan Publik.</li>
                        <li>Pejabat Pengelola Informasi dan Dokumentasi yang selanjutnya disebut PPID adalah pejabat struktural yang bertanggung jawab dalam pengumpulan, pendokumentasian, penyimpanan, pemeliharaan, penyediaan, distribusi dan pelayanan informasi di Pemerintahan Daerah.</li>
                        <li>PPID Pembantu adalah pejabat struktural yang bertanggung jawab dalam pengumpulan, pendokumentasian, penyimpanan, pemeliharaan, penyediaan, distribusi dan pelayanan informasi di lingkungan SKPD.</li>
                        <li>Atasan PPID adalah pejabat yang merupakan atasan langsung PPID.</li>
                        <li>Daftar Informasi Publik adalah catatan yang berisi keterangan secara sistematis tentang seluruh Informasi Publik yang berada dibawah penguasaan Pemerintah Daerah tidak termasuk informasi yang dikecualikan.</li>
                        <li>Pengguna Informasi Publik adalah orang yang menggunakan informasi publik sebagaimana diatur dalam ketentuan peraturan perundang-undangan.</li>
                        <li>Orang adalah orang perseorangan, kelompok orang, badan hukum, atau badan publik.</li>
                        <li>Pemohon Informasi Publik adalah warga negara dan/atau badan hukum Indonesia yang mengajukan permintaan informasi publik sebagaimana diatur dalam ketentuan peraturan perundang-undangan.</li>
                    </ol>
                </div>
                <div class="text-center mt-10" style="font-weight: bold">
                    <h4>BAB II</h4>
                    <h4>Pengelola Layanan Informasi dan Dokumentasi</h4>
                </div>
                <div class="mt-10">
                    <strong>Pasal 2:</strong>
                    <p>Pemerintahan Daerah yang terdiri dari Pemerintah Daerah dan Dewan Perwakilan Rakyat Daerah sebagai Badan Publik melaksanakan pengelolaan pelayanan informasi dan dokumentasi.</p>
                    <strong>Pasal 3:</strong>
                    <ol>
                        <li>Informasi Publik di lingkungan Pemerintahan Daerah bersifat terbuka dan dapat diakses oleh setiap Pengguna Informasi Publik.</li>
                        <li>Informasi Publik yang dikecualikan di lingkungan Pemerintahan Daerah bersifat ketat dan terbatas.</li>
                        <li>Informasi Publik di lingkungan Pemerintahan Daerah dapat diperoleh pemohon informasi publik dengan cepat, tepat waktu, biaya ringan, dan dapat diakses dengan mudah.</li>
                        <li>Informasi Publik yang dikecualikan di lingkungan Pemerintahan Daerah bersifat rahasia sesuai dengan Peraturan Perundang - undangan , kepatuhan, dan kepentingan umum.</li>
                        <li>Informasi Publik yang dikecualikan didasarkan pada pengujian atas konsekuensi yang timbul apabila suatu informasi diberikan kepada masyarakat dan pertimbangan yang seksama bahwa menutup informasi publik dapat melindungi kepentingan yang lebih besar daripada membukanya atau sebaliknya.</li>
                    </ol>
                </div>

                <div class="text-center mt-10" style="font-weight: bold">
                    <h4>BAB III</h4>
                    <h4>Hak dan Kewajiban</h4>
                </div>
                <div class="mt-10">
                    <strong>Pasal 4:</strong>
                    <ol>
                        <li>Pemerintah Daerah berhak menolak memberikan informasi yang dikecualikan.</li>
                        <li>Informasi yang dikecualikan sebagaimana dimaksud pada ayat (1) ditetapkan lebih lanjut oleh Bupati sesuai dengan kewenangannya.</li>
                        <li>Pemerintah Daerah berhak menolak memberikan informasi publik apabila tidak sesuai dengan Peraturan Perundang - undangan.</li>
                        <li>Informasi yang tidak dapat diberikan oleh Pemerintah Daerah, sebagaimana dimaksud pada ayat (1) adalah :</li>
                        <ol style="list-style-type: upper-alpha">
                            <li>Informasi yang dapat membahayakan negara.</li>
                            <li>Informasi yang dapat berkaitan dengan kepentingan perlindungan usaha dari persaingan usaha tidak sehat.</li>
                            <li>Informasi yang berkaitan dengan hak - hak pribadi .</li>
                            <li>Informasi yang berkaitan dengan rahasia jabatan atau</li>
                            <li>Informasi yang belum dikuasai atau didokumentasikan.</li>
                        </ol>
                    </ol>
                    <strong>Pasal 5:</strong>
                    <ol>
                        <li>Pemerintah Daerah wajib menyediakan, memberikan dan / atau menerbitkan informasi publik yang berada dibawah kewenangannya kepada pemohon informasi publik, selain informasi yang dikecualikan sesuai dengan Peraturan Perundang - undangan.</li>
                        <li>Untuk melaksanakan kewajiban sebagaimana dimaksud pada ayat (1), Pemerintah Daerah membangun dan mengembangkan sistem informasi dan dokumentasi pengelolaan informasi publik yang dapat diakses dengan mudah.</li>
                    </ol>
                </div>

                <div class="text-center mt-10" style="font-weight: bold">
                    <h4>BAB IV</h4>
                    <h4>Pejabat Pengelola Informasi dan Dokumentasi</h4>
                </div>
                <div class="mt-10">
                    <strong>Pasal 6:</strong>
                    <ol>
                        <li>Untuk mengelola pelayanan informasi dan dokumentasi di lingkungan Pemerintahan Daerah ditetapkan PPID.</li>
                        <li>PPID sebagaimana dimaksud pada ayat (1) melekat pada pejabat struktural yang membidangi tugas dan fungsi pelayanan informasi.</li>
                        <li>PPID ditetapkan oleh Bupati.</li>
                    </ol>
                    <strong>Pasal 7:</strong>
                    <ol>
                        <li>PPID bertanggungjawab kepada Bupati melalui Sekretaris Daerah.</li>
                        <li>PPID dibantu oleh PPID pembantu yang berada di lingkungan SKPD.</li>
                    </ol>
                    <strong>Pasal 8:</strong>
                    <p style="margin: 0">PPID Bertugas:</p>
                    <ol style="list-style-type: upper-alpha">
                        <li>Mengkoordinasikan dan mengkonsolidasikan pengumpulan bahan informasi dan dokumentasi dari PPID Pembantu.</li>
                        <li>Menyimpan, mendokumentasikan, menyediakan dan memberi pelayanan informasi kepada publik.</li>
                        <li>Melakukan verifikasi bahan informasi publik.</li>
                        <li>Melakukan uji konsekuensi atas informasi yang dikecualikan.</li>
                        <li>Melakukan pemutakhiran informasi dan dokumentasi dan</li>
                        <li>Menyediakan informasi dan dokumentasi untuk diakses oleh masyarakat.</li>
                    </ol>

                    <strong>Pasal 9:</strong>
                    <p style="margin: 0">Dalam Rangka melaksanakan tugas sebagaimana dimaksud dalam Pasal 8, PPID berwenang:</p>
                    <ol style="list-style-type: upper-alpha">
                        <li>Menolak memberikan informasi yang dikecualikan sesuai ketentuan Peraturan Perundang-undangan.</li>
                        <li>Meminta dan memperoleh informasi dari unit kerja/ komponen/ satuan kerja yang menjadi cakupan kerjanya.</li>
                        <li>Mengkoordinasikan pemberian pelayanan informasi dengan PPID Pembantu dan/ atau Pejabat Fungsional yang menjadi cakupan kerjanya.</li>
                        <li>Menentukan atau menetapkan suatu informasi dapat/ tidaknya diakses publik.</li>
                        <li>Menugaskan PPID Pembantu dan/atau Pejabat Fungsional untuk membuat, mengumpulkan, serta memelihara informasi dan dokumentasi untuk kebutuhan organisasi.</li>
                    </ol>

                    <strong>Pasal 10:</strong>
                    <ol>
                        <li>PPID Pembantu sebagaimana tersebut pada Pasal 7 ayat (2) mempunyai kriteria sebagai berikut :</li>
                        <ol style="list-style-type: upper-alpha">
                            <li>Merupakan pejabat struktural yang melaksanakan tugas pokok dan fungsi PPID pada SKPD.</li>
                            <li>PPID Pembantu dijabat oleh Sekretaris atau Kepala Tata Usaha pada SKPD.</li>
                        </ol>
                        <li>PPID Pembantu ditetapkan dengan Keputusan Kepala SKPD;</li>
                        <li>PPID Pembantu bertugas membantu PPIDmelaksanakan ketentuan sebagaimana dimaksud dalam Pasal 8 dan Pasal 9;</li>
                        <li>PPID Pembantu menyampaikan informasi dan dokumentasi kepada PPID secara berkala dan sesuai kebutuhan.</li>
                    </ol>

                    <strong>Pasal 11:</strong>
                    <p>Tata kerja PPID di lingkungan Pemerintah Daerah diatur oleh Bupati sesuai kewenangannya.</p>
                </div>

                <div class="text-center mt-10" style="font-weight: bold">
                    <h4>BAB V</h4>
                    <h4>Pemohon Informasi dan Dokumentasi</h4>
                </div>
                <div class="mt-10">
                    <strong>Pasal 12:</strong>
                    <p style="margin: 0">Pemohon informasi dan dokumentasi meliputi :</p>
                    <ol style="list-style-type: upper-alpha">
                        <li>Perseorangan.</li>
                        <li>Kelompok Masyarakat.</li>
                        <li>Lembaga Swadaya masyarakat.</li>
                        <li>Organisasi masyarakat.</li>
                        <li>Partai Politik atau</li>
                        <li>Badan Publik lainnya.</li>
                    </ol>
                    <strong>Pasal 13:</strong>
                    <p style="margin: 0">Pemohon informasi dan dokumentasi kepada pemerintahan daerah harus memenuhi persyaratan :</p>
                    <ol style="list-style-type: upper-alpha">
                        <li>Mencantumkan identitas yang jelas.</li>
                        <li>Mencantumkan alamat dan nomor telepon.</li>
                        <li>Menyampaikan secara jelas jenis informasi dan dokumentasi yang dibutuhkan dan</li>
                        <li>Mencantumkan maksud dan tujuan permohonan informasi dan dokumentasi .</li>
                    </ol>
                </div>

                <div class="text-center mt-10" style="font-weight: bold">
                    <h4>BAB VI</h4>
                    <h4>Klasifikasi Informasi Publik</h4>
                    <h4>Bagian Kesatu</h4>
                    <h4>Umum</h4>
                </div>
                <div class="mt-10">
                    <strong>Pasal 14:</strong>
                    <p>Informasi publik di lingkungan Pemerintah Daerah merupakan informasi yang dihasilkan, disimpan, dikelola, dikirim, atau diterima, yang berkaitan dengan kegiatan penyelenggaraan pemerintahan di Daerah.</p>
                    <strong>Pasal 15:</strong>
                    <p style="margin: 0">Informasi public sebagaimana dimaksud dalam Pasal 14 , diklasifikasikan menjadi 4 (empat) kelompok :</p>
                    <ol style="list-style-type: upper-alpha">
                        <li>Informasi yang wajib disediakan dan diumumkan secara berkala.</li>
                        <li>Informasi yang wajib diumumkan secara serta merta.</li>
                        <li>Informasi yang wajib tersedia setiap saat.</li>
                        <li>Informasi yang dikecualikan.</li>
                    </ol>
                </div>

                <div class="text-center mt-10" style="font-weight: bold">
                    <h4>Bagian Kedua</h4>
                    <h4>Informasi yang Wajib Disediakan dan Diumumkan secara Berkala</h4>
                </div>
                <div class="mt-10">
                    <strong>Pasal 16:</strong>
                    <p style="margin: 0">Pemohon informasi dan dokumentasi meliputi :</p>
                    <ol>
                        <li>Informasi yang wajib disediakan dan diumumkan secara berkala, sekurang-kurangnya terdiri atas :</li>
                        <ol style="list-style-type: upper-alpha">
                            <li>Informasi tentang profil badan publik yang meliputi :</li>
                            <ol>
                                <li>Informasi tentang kedudukan atau domisili beserta alamat lengkap, ruang lingkup kegiatan, maksud dan tujuan, tugas dan fungsi Badan publik beserta unit-unit kerja di bawahnya.</li>
                                <li>Struktur organisasi, gambaran umum.</li>
                            </ol>
                            <li>Ringkasan informasi tentang program dan/atau kegiatanyang sedang dijalankan dalam badan publik,terdiri atas</li>
                            <ol>
                                <li>Nama program dan kegiatan.</li>
                                <li>Penanggungjawab, pelaksana program dan kegiatan.</li>
                                <li>Target dan/atau capaian program dan kegiatan.</li>
                                <li>Jadwal pelaksanaan program dan kegiatan.</li>
                                <li>Anggaran program dan kegiatan yang meliputi sumber dan jumlah.</li>
                                <li>Agenda penting terkait pelaksanaan tugas badan publik</li>
                            </ol>
                            <li>Ringkasan informasi tentang kinerja dalam lingkup badan publik.</li>
                            <li>Ringkasan laporan keuangan.</li>
                            <li>Ringkasan laporan akses Informasi Publik,terdiri atas :</li>
                            <ol>
                                <li>Jumlah permohonan Informasi Publik yang diterima.</li>
                                <li>Waktu yang diperlukan dalam memenuhi setiap permohonan Informasi Publik.</li>
                                <li>Jumlah permohonan Informasi Publik yang dikabulkan baik sebagian atau seluruhnya dan permohonan Informasi Publik yang ditolak.</li>
                                <li>Alasan penolakan permohonan Informasi Publik.</li>
                            </ol>
                            <li>Informasi tentang peraturan, keputusan dan/ataukebijakanyang mengikat dan/atau berdampak bagi publik yang dikeluarkan oleh badan publik,terdiri atas :</li>
                            <ol>
                                <li>Daftar rancangan Peraturan Perundang-undangan Daerah, yang sedang dalam proses pembuatan.</li>
                                <li>Daftar Peraturan Perundang-undangan, yang telah diundangkan.</li>
                            </ol>
                            <li>Informasi tentang hak dan tata cara memperoleh Informasi Publik serta tata cara,pengajuan keberatan serta proses penyelesaian sengketa Informasi Publik berikut pihak-pihak yang bertanggung jawab yang dapat dihubungi.</li>
                            <li>Informasi tentang tata cara pelayanan pengaduan.</li>
                            <li>Informasi tentang pengumuman pengadaan barang dan/atau jasa sesuai dengan peraturan perundang-undangan terkait.</li>
                        </ol>
                        <li>Pengumuman secara berkala sebagaimana dimaksud pada ayat (1), dilakukan sekurang-kurangnya setiap 1 (satu) tahun sekali.</li>
                    </ol>
                </div>

                <div class="text-center mt-10" style="font-weight: bold">
                    <h4>Bagian Ketiga</h4>
                    <h4>Informasi yang Wajib Diumumkan secara Serta Merta</h4>
                </div>
                <div class="mt-10">
                    <strong>Pasal 17:</strong>
                    <ol>
                        <li>Setiap Badan Publik wajib mengumumkan secara serta merta, yaitu suatu informasi yang dapat mengancam hajat hidup orang banyak dan ketertiban umum antara lain :</li>
                        <ol style="list-style-type: upper-alpha">
                            <li>Informasi tentang bencana alam seperti kekeringan, kebakaran hutan karena factor alam, hama penyakit tanaman, epidemik, wabah dan kejadian luarbiasa.</li>
                            <li>Informasi tentang keadaan bencana non alam seperti kegagalan industri atau teknologi, dampak industri, dan pencemaran lingkungan.</li>
                            <li>Bencana sosial seperti kerusuhan sosial, konflik sosial antar kelompok atau antar komunitas masyarakat dan teror.</li>
                            <li>Informasi tentang jenis, persebaran dan daerah yang menjadi sumber penyakit yang berpotensi menular.</li>
                            <li>Informasi tentang racun pada bahan makanan yang dikonsumsi oleh masyarakat atau</li>
                            <li>Informasi tentang rencana gangguan terhadap putilitas publik.</li>
                        </ol>
                        <li>Pengumuman informasi sebagaimana dimaksud pada ayat (1), disampaikan dengan bahasa yang sederhana, mudah dimengerti dan dipahami oleh masyarakat.</li>
                    </ol>

                    <strong>Pasal 18:</strong>
                    <ol>
                        <li>Pihak yang menerima izin atau perjanjian kerja dari badan publik yang kegiatannya berpotensi mengancam hajat hidup orang banyak dan ketertiban umum sebagaimana dimaksud dalam Pasal 31 ayat (1), wajib memiliki Standar pengumuman informasi;</li>
                        <li>Standar pengumuman informasi sebagaimana dimaksud pada ayat (1),sekurang-kurangnya meliputi:</li>
                        <ol style="list-style-type: upper-alpha">
                            <li>Potensi bahaya dan/atau besaran dampak yang dapat ditimbulkan;</li>
                            <li>Pihak-pihak yang berpotensi terkena dampak baik masyarakat umum maupun pegawai badan publik yang menerima izin atau perjanjian kerja dari badan publik tersebut;</li>
                            <li>Prosedur dan tempat evakuasi apabila keadaan darurat terjadi;</li>
